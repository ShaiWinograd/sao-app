function parseDateKey(dateKey: string) {
  const [year, month, day] = dateKey.split('-').map(Number);
  return new Date(year, month - 1, day);
}

export function getNonWorkingDayLabel(dateKey: string) {
  if (parseDateKey(dateKey).getDay() === 6) {
    return 'שבת';
  }

  return NON_WORKING_HOLIDAYS[dateKey] ?? null;
}

export function isWorkCreationBlockedDay(dateKey: string) {
  const label = getNonWorkingDayLabel(dateKey);
  if (!label) return false;
  return label === 'שבת' || !label.startsWith('ערב ');
}
